    public void crearColumna() {
        ArrayList<String> nombreColumna = new ArrayList<>();

        nombreColumna.add("ID");
        nombreColumna.add("Identificacion");
        nombreColumna.add("Nombre");
        nombreColumna.add("Cargo");

        for (String columna : nombreColumna) {
            modeloTabla.addColumn(columna);
        }
        this.tabla.setModel(modeloTabla);
    }

    public void cargarTabla() {
        ArrayList<Colaborador> listaCliente = LN.consultar();

        ArrayList<Object[]> datos = new ArrayList<>();

        if (!listaCliente.isEmpty()) {
            for (Colaborador c : listaCliente) {
                Object[] colab = new Object[]{
                    c.getIdColaborador(),
                    c.getIdentificacion(),
                    c.getNombreColaborador(),
                    c.getCargo()
                };
                datos.add(colab);
            }
            for (Object[] colab : datos) {
                modeloTabla.addRow(colab);
            }
            tabla.setModel(modeloTabla);
        }
    }

