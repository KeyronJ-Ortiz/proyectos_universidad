    public boolean verificarVaciosGuardar() {
        boolean hayVacio = false;
        if (txtNombre.getText().equals("")
                || cbCargo.getSelectedItem().equals("Seleccione un cargo")
                || txtIdentificacion.getText().equals("")) {
            hayVacio = true;

        }
        return hayVacio;
    }

    public boolean verificarVaciosModificar() {
        boolean hayVacio = false;
        if (txtModificarNombre.getText().equals("")
                || cbModificarCargo.getSelectedItem().equals("Seleccione al cargo que desea cambiar")
                || txtModificarIdentificacion.getText().equals("")) {
            hayVacio = true;

        }
        return hayVacio;
    }

    public void limpiar() {
        txtNombre.setText("");
        txtIdentificacion.setText("");
        cbCargo.removeAllItems();
        cbCargo.addItem("Seleccione un cargo");
        cbCargo.addItem("Recepcionista");
        cbCargo.addItem("Guarda");
        cbCargo.addItem("Cocinero");
        cbCargo.addItem("Limpieza");

    }

    public void limpiarTodo() {
        txtNombre.setText("");
        txtIdentificacion.setText("");
        cbCargo.removeAllItems();
        cbCargo.addItem("Seleccione un cargo");
        cbCargo.addItem("Recepcionista");
        cbCargo.addItem("Guarda");
        cbCargo.addItem("Cocinero");
        cbCargo.addItem("Limpieza");
        txtModificar.setText("");
        txtModificarNombre.setText("");
        txtModificarIdentificacion.setText("");

        cbModificarCargo.removeAllItems();

        cbModificarCargo.addItem("Seleccione al cargo que desea cambiar");
        cbModificarCargo.addItem("Recepcionista");
        cbModificarCargo.addItem("Guarda");
        cbModificarCargo.addItem("Cocinero");
        cbModificarCargo.addItem("Limpieza");
        txtEliminar.setText("");
    }

    public void limpiarModificar() {
        txtModificar.setText("");
        txtModificarNombre.setText("");
        txtModificarIdentificacion.setText("");

        cbModificarCargo.removeAllItems();

        cbModificarCargo.addItem("Seleccione al cargo que desea cambiar");
        cbModificarCargo.addItem("Recepcionista");
        cbModificarCargo.addItem("Guarda");
        cbModificarCargo.addItem("Cocinero");
        cbModificarCargo.addItem("Limpieza");
    }

