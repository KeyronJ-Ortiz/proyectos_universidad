    public void cargarTabla() {
        ArrayList<Habitaciones> listaHabitacion = habiLN.consultar();

        ArrayList<Object[]> datos = new ArrayList<>();

        if (!listaHabitacion.isEmpty()) {
            for (Habitaciones h : listaHabitacion) {
                Object[] habi = new Object[]{
                    h.getIdHabitacion(),
                    h.getCostoDeHabitacion(),
                    h.getCantidadMaximaDePersonas(),
                    h.getPersonaQueReserva(),
                    h.getTipoDeCama(),
                    h.getAireAcondicionado(),
                    h.getEstadoDeHabitacion()
                };
                datos.add(habi);
            }
            for (Object[] habi : datos) {
                modeloTabla.addRow(habi);
            }
            tabla.setModel(modeloTabla);
        }
    }

    public void crearModeloColumna() {
        ArrayList<String> nombreColumna = new ArrayList<>();

        nombreColumna.add("ID Habitacion");
        nombreColumna.add("Costo");
        nombreColumna.add("CantidadPersonas");
        nombreColumna.add("Persona que reserva");
        nombreColumna.add("Tipo de cama");
        nombreColumna.add("Aire acondicionado");
        nombreColumna.add("Estado habitacion");

        for (String columna : nombreColumna) {
            modeloTabla.addColumn(columna);
        }
        this.tabla.setModel(modeloTabla);
    }

