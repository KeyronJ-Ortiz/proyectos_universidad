    private void btnReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservarActionPerformed
        if (!verificarCampoVacioReservar()) {
            habi = new Habitaciones();

            habi.setIdHabitacion(Integer.parseInt(txtReservar.getText()));
            habi.setEstadoDeHabitacion("Reservada");
            habi.setCantidadMaximaDePersonas(Integer.parseInt((String) cbCantidadDePersonas.getSelectedItem()));
            habi.setTipoDeCama((String) cbTipoCama.getSelectedItem());
            cliente.setIdCliente(Integer.parseInt(txtIdCliente.getText()));

            if (habi.getTipoDeCama().equalsIgnoreCase("1 cama matrimonial con aire")) {
                habi.setCostoDeHabitacion(35000);

            } else if (habi.getTipoDeCama().equalsIgnoreCase("2 camas matrimoniales con aire")) {
                habi.setCostoDeHabitacion(65000);
            } else if (habi.getTipoDeCama().equalsIgnoreCase("2 camas matrimoniales y 2 camarotes con aire")) {
                habi.setCostoDeHabitacion(100000);
            }
            habi.setAireAcondicionado(true);

            limpiarTodo();

            JOptionPane.showMessageDialog(null, "El costo de la habitacion es : " + habi.getCostoDeHabitacion());

            modeloTabla.setRowCount(0);

            if (habiLN.buscarHabitacion(habi.getIdHabitacion())) {

                if (clienteLN.buscarIdCliente(cliente.getIdCliente())) {

                    if (habiLN.verificarDisponible(habi.getIdHabitacion())) {
                        limpiarTodo();
                        habi.setPersonaQueReserva(clienteLN.sacarNombreCliente(cliente.getIdCliente()));
                        modeloTabla.setRowCount(0);
                        JOptionPane.showMessageDialog(null, habiLN.modificarHabitacion(habi) ? "Se reservo con exito" : "No se pudo reservar");
                        cargarTabla();

                    } else {
                        modeloTabla.setRowCount(0);
                        JOptionPane.showMessageDialog(null, "No puede reservar una habitacion con un estado diferente a disponible");
                        cargarTabla();
                    }

                } else {
                    limpiarTodo();
                    modeloTabla.setRowCount(0);
                    JOptionPane.showMessageDialog(null, "No existe id, intente registrarse en el hotel");
                    cargarTabla();
                }
            } else {
                modeloTabla.setRowCount(0);
                JOptionPane.showMessageDialog(null, "No existe la habitacion");
                cargarTabla();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Asigne los datos correspondientes");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnReservarActionPerformed

