    public void limpiarTodo() {
        txtIdCliente.setText("");
        txtMasajes.setText("");
        txtTransporte.setText("");
        txtTelefono.setText("");
        txtCajaSeguridad.setText("");
        txtEliminar.setText("");
        txtModificar.setText("");
        txtBuscarIdCliente.setText("");
        txtIdHabitacion.setText("");
    }

    public boolean vacioAgregar() {
        boolean estaVacio = false;
        if (txtIdCliente.getText().equals("")
                || txtMasajes.getText().equals("")
                || txtTransporte.getText().equals("")
                || txtTelefono.getText().equals("")
                || txtCajaSeguridad.getText().equals("")
                || txtIdHabitacion.getText().equals("")) {
            estaVacio = true;
        }
        return estaVacio;
    }

    public boolean vacioModificar() {
        boolean estaVacio = false;
        if (txtModificar.getText().equals("")
                || txtIdCliente.getText().equals("")
                || txtMasajes.getText().equals("")
                || txtTransporte.getText().equals("")
                || txtTelefono.getText().equals("")
                || txtCajaSeguridad.getText().equals("")
                || txtIdHabitacion.getText().equals("")) {
            estaVacio = true;
        }
        return estaVacio;
    }

    public boolean vacioEliminar() {
        boolean estaVacio = false;
        if (txtEliminar.getText().equals("")) {
            estaVacio = true;
        }
        return estaVacio;
    }

