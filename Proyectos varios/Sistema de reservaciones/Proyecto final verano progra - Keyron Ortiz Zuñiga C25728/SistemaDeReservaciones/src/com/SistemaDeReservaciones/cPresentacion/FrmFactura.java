    public void crearColumna() {
        ArrayList<String> nombreColumna = new ArrayList<>();

        nombreColumna.add("ID Factura");
        nombreColumna.add("ID Cliente");
        nombreColumna.add("ID Habitacion");
        nombreColumna.add("Nombre");
        nombreColumna.add("Masajes");
        nombreColumna.add("Transporte");
        nombreColumna.add("Telefono");
        nombreColumna.add("Caja de Seguridad");
        nombreColumna.add("Precio habitacion");
        nombreColumna.add("Precio total");

        for (String columna : nombreColumna) {
            modeloTabla.addColumn(columna);
        }
        this.tabla.setModel(modeloTabla);
    }

    public void cargarTabla() {
        ArrayList<Factura> facturas = factuLN.consultar();

        ArrayList<Object[]> datos = new ArrayList<>();

        if (!facturas.isEmpty()) {
            for (Factura f : facturas) {
                Object[] factu = new Object[]{
                    f.getIdFactura(),
                    f.getIdCliente(),
                    f.getIdHabitacion(),
                    f.getNombrePersonaFactura(),
                    f.getCantidadMasajes(),
                    f.getCuantasVecesUsoTransporte(),
                    f.getCuantasVecesUsoTelefono(),
                    f.getCuantasVecesUsoCajaSeguridad(),
                    f.getPrecioHabitacion(),
                    f.getPrecioTotal()
                };
                datos.add(factu);
            }
            for (Object[] factu : datos) {
                modeloTabla.addRow(factu);
            }
            tabla.setModel(modeloTabla);
        }
    }

