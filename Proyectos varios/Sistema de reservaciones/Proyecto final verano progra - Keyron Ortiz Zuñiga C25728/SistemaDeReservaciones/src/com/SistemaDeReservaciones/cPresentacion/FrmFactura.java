    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (!vacioAgregar()) {
            factu = new Factura();

            factu.setIdFactura(Integer.parseInt(txtIdFactura.getText()));
            factu.setIdCliente(Integer.parseInt(txtIdCliente.getText()));
            factu.setIdHabitacion(Integer.parseInt(txtIdHabitacion.getText()));

            factu.setCantidadMasajes(Integer.parseInt(txtMasajes.getText()));
            factu.setCuantasVecesUsoTransporte(Integer.parseInt(txtTransporte.getText()));
            factu.setCuantasVecesUsoTelefono(Integer.parseInt(txtTelefono.getText()));
            factu.setCuantasVecesUsoCajaSeguridad(Integer.parseInt(txtCajaSeguridad.getText()));

            if (factuLN.verificarHabitacionDeLujo(factu.getIdHabitacion())) {

                if (factuLN.buscarIdHabitacionDeLujo(factu.getIdHabitacion())) {

                    if (factuLN.verificarFacturableDeLujo(factu.getIdHabitacion())) {

                        if (factuLN.buscarIdCliente(factu.getIdCliente())) {

                            factu.setPrecioTotal(factuLN.calcularUsos(factu.getCantidadMasajes(), factu.getCuantasVecesUsoTransporte(), factu.getCuantasVecesUsoCajaSeguridad(), factu.getCuantasVecesUsoTelefono(), habi.getCostoDeHabitacion()));
                            factu.setNombrePersonaFactura(factuLN.obtenerNombreLujo(factu.getIdHabitacion()));
                            factu.setPrecioHabitacion(factuLN.obtenerPrecioHabitacionLujosa(factu.getIdHabitacion()));
                            modeloTabla.setRowCount(0);
                            limpiarTodo();
                            JOptionPane.showMessageDialog(null, factuLN.agregarFactura(factu, true) ? "La factura se agrego con exito." : "No se agrego la factura");
                            txtIdFactura.setText(factuLN.consecutivoId() + "");
                            cargarTabla();
                        } else {
                            modeloTabla.setRowCount(0);
                            JOptionPane.showMessageDialog(null, "No existe id");
                            cargarTabla();
                        }
                    } else {
                        modeloTabla.setRowCount(0);
                        JOptionPane.showMessageDialog(null, "No se puede efectuar la factura de una habitacion que no esta reservada o ocupada");
                        cargarTabla();
                    }
                } else {
                    modeloTabla.setRowCount(0);
                    JOptionPane.showMessageDialog(null, "No se encontro habitacion");
                    cargarTabla();
                }
            } else {
                if (factuLN.buscarIdHabitacionSencilla(factu.getIdHabitacion())) {

                    if (factuLN.buscarIdHabitacionSencilla(factu.getIdHabitacion())) {

                        if (factuLN.buscarIdCliente(factu.getIdCliente())) {

                            factu.setPrecioTotal(factuLN.calcularUsos(factu.getCantidadMasajes(), factu.getCuantasVecesUsoTransporte(), factu.getCuantasVecesUsoCajaSeguridad(), factu.getCuantasVecesUsoTelefono(), habi.getCostoDeHabitacion()));
                            factu.setNombrePersonaFactura(factuLN.obtenerNombreSencilla(factu.getIdHabitacion()));
                            factu.setPrecioHabitacion(factuLN.obtenerPrecioHabitacionSencilla(factu.getIdHabitacion()));

                            limpiarTodo();

                            modeloTabla.setRowCount(0);
                            JOptionPane.showMessageDialog(null, factuLN.agregarFactura(factu, true) ? "La factura se agrego con exito." : "No se agrego la factura");
                            txtIdFactura.setText(factuLN.consecutivoId() + "");
                            cargarTabla();
                        } else {
                            modeloTabla.setRowCount(0);
                            JOptionPane.showMessageDialog(null, "No existe id");
                            cargarTabla();
                        }
                    } else {
                        modeloTabla.setRowCount(0);
                        JOptionPane.showMessageDialog(null, "No se puede efectuar la factura de una habitacion que no esta reservada o ocupada");
                        cargarTabla();
                    }
                } else {
                    modeloTabla.setRowCount(0);
                    JOptionPane.showMessageDialog(null, "No se encontro habitacion");
                    cargarTabla();
                }
            }
        } else {
            modeloTabla.setRowCount(0);
            JOptionPane.showMessageDialog(null, "Rellene los campos vacios");
            cargarTabla();
        } // TODO add your handling code here:
    }//GEN-LAST:event_btnAgregarActionPerformed

